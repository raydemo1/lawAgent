/**
 * A group of citations sharing the same usage category.
 *
 * Matches `CitationGroup` in `law_agent/review/schemas.py`.
 */
export interface CitationGroup {
  usage: CitationUsage;
  citations: Citation[];
  /** Optional note describing the scope of this group. */
  scope_note: string | null;
}

/**
 * A specific evidence sufficiency issue detected during self-check.
 *
 * Matches `EvidenceIssue` in `law_agent/review/schemas.py`.
 */
export interface EvidenceIssue {
  issue_type: EvidenceIssueType;
  description: string;
}

/**
 * A typed query generated for retrieval.
 *
 * Matches `RetrievalQuery` in `law_agent/review/schemas.py`. Referenced by
 * `SecondRetrievalPlan.expanded_queries`.
 */
export interface RetrievalQuery {
  query_id: string;
  query_type: RetrievalQueryType;
  text: string;
}

/**
 * A scored evidence hit returned by a retriever.
 *
 * Matches `RetrievalHit` in `law_agent/review/schemas.py`. Surfaced in the
 * review response as `evidence_chunks` so the frontend can expand a citation
 * to show the underlying clause text, source URL, and retrieval metadata.
 */
export interface RetrievalHit {
  chunk_id: string;
  doc_id: string;
  source_id: string;
  title: string;
  text: string;
  score: number;
  rank: number;
  retriever: RetrieverName;
  citation_role: ClauseCitationRole;
  can_cite_clause: boolean;
  source_url: string;
  matched_query_type: RetrievalQueryType | null;
}

/**
 * Chunk-level evidence retained for one selected source.
 *
 * The backend uses source-aware fusion to pick diverse sources, then attaches
 * same-source supporting chunks and neighbor chunks so the report and UI keep
 * the precise passages behind each source.
 */
export interface SourceEvidencePacket {
  source_id: string;
  title: string;
  representative_chunk: RetrievalHit;
  supporting_chunks: RetrievalHit[];
  neighbor_chunks: RetrievalHit[];
}

/**
 * Plan for one controlled second retrieval when evidence is weak.
 *
 * Matches `SecondRetrievalPlan` in `law_agent/review/schemas.py`.
 */
export interface SecondRetrievalPlan {
  expanded_queries: RetrievalQuery[];
  increased_top_k: number;
  stronger_boost: boolean;
  reason: string;
}

/**
 * Evidence sufficiency state for a review trace.
 *
 * Matches `EvidenceSelfCheck` in `law_agent/review/schemas.py`.
 */
export interface EvidenceSelfCheck {
  status: EvidenceStatus;
  issues: EvidenceIssue[];
  triggered_reasons: string[];
  second_retrieval_triggered: boolean;
  /** Present only when a second retrieval is planned. */
  second_retrieval_plan: SecondRetrievalPlan | null;
}

/**
 * Structured review result produced from facts and evidence.
 *
 * Matches `ReviewResult` in `law_agent/review/schemas.py`.
 */
export interface ReviewResult {
  review_result_id: string;
  review_case_id: string;
  trace_id: string;
  risk_level: RiskLevel;
  conclusion: string;
  review_facts: ReviewFacts;
  trigger_reasons: string[];
  missing_information: string[];
  recommended_actions: string[];
  risk_boundaries: string[];
  citations: Citation[];
  /** Evidence grouped by usage category. */
  applicable_evidence: CitationGroup[];
}

/**
 * Response body for `POST /api/review`.
 *
 * Matches `ReviewResponse` in `law_agent/review/api.py`.
 */
export interface ReviewResponse {
  review_case_id: string;
  trace_id: string;
  review_facts: ReviewFacts;
  review_result: ReviewResult;
  evidence_self_check: EvidenceSelfCheck;
  /** Same value as `review_result.applicable_evidence`, surfaced for convenience. */
  citation_groups: CitationGroup[];
  second_retrieval_triggered: boolean;
  /** Typed retrieval queries planned from the question + facts (query plan). */
  retrieval_queries?: RetrievalQuery[];
  /** Final evidence hits with chunk text, used to expand citations inline. */
  evidence_chunks: RetrievalHit[];
  /** Source-level evidence packets with representative/supporting/neighbor chunks. */
  source_evidence_packets: SourceEvidencePacket[];
}

/**
 * Structured failure response for LLM-owned workflow nodes.
 *
 * The backend intentionally returns this as HTTP 200 so the UI can display
 * which node failed after retries instead of treating it as transport failure.
 */
export interface ReviewFailedResponse {
  status: 'review_failed';
  failed_node: string;
  reason: string;
  message: string;
  attempts: number;
  trace_id: string | null;
}

/** Response body for `POST /api/review`. */
export type ReviewApiResponse = ReviewResponse | ReviewFailedResponse;

export function isReviewFailedResponse(
  response: ReviewApiResponse | null,
): response is ReviewFailedResponse {
  return Boolean(response && 'status' in response && response.status === 'review_failed');
}

/**
 * Response body for `GET /api/health`.
 *
 * Matches `HealthResponse` in `law_agent/review/api.py`.
 */
export interface HealthResponse {
  status: string;
}

