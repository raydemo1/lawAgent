/**
 * Structured facts extracted from the user-supplied material.
 *
 * Matches `ReviewFacts` in `law_agent/review/schemas.py`. All optional
 * fields are `null` when the fact could not be inferred from the material.
 */
export interface ReviewFacts {
  /** High-level description of the business activity, if detected. */
  business_activity: string | null;
  /** Data categories referenced by the material (e.g. "personal_info"). */
  data_types: string[];
  /** Whether sensitive personal information is involved. */
  sensitive_personal_info: boolean | null;
  /** Whether cross-border data transfer is involved. */
  cross_border_transfer: boolean | null;
  /** The overseas recipient, if any. */
  overseas_recipient: string | null;
  /** Stated processing purpose. */
  processing_purpose: string | null;
  /** Legal basis or consent description claimed by the material. */
  legal_basis_or_consent: string | null;
  /** Industry context (e.g. "finance", "healthcare"). */
  industry: string | null;
  /** Geographic region context (e.g. "CN", "EU"). */
  region: string | null;
  /** Facts that could not be determined and should be requested from the user. */
  missing_information: string[];
}

