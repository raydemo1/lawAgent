/**
 * TypeScript type definitions for the LawAgent review API.
 *
 * These interfaces mirror the Pydantic response models exposed by the
 * FastAPI backend in `law_agent/review/api.py` and its dependencies
 * (`law_agent/review/schemas.py`, `law_agent/review/evalset/schemas.py`,
 * `law_agent/data/schemas.py`).
 *
 * Field names and JSON shapes are kept in sync with the backend so that
 * responses can be consumed directly without any client-side mapping.
 */

// ---------------------------------------------------------------------------
// Shared literal union types
// ---------------------------------------------------------------------------

/** Risk level reported by a review result. */
export type RiskLevel =
  | 'high'
  | 'medium'
  | 'low'
  | 'insufficient_evidence';

/** Usage category for a group of citations. */
export type CitationUsage =
  | 'legal_basis'
  | 'conditional_basis'
  | 'implementation_reference'
  | 'policy_explanation';

/** Role a clause citation plays in the legal argument. */
export type ClauseCitationRole =
  | 'primary_legal_basis'
  | 'conditional_local_basis'
  | 'conditional_industry_basis'
  | 'implementation_reference'
  | 'interpretation_auxiliary';

/** Status of the evidence sufficiency self-check. */
export type EvidenceStatus =
  | 'not_checked'
  | 'sufficient'
  | 'needs_second_retrieval'
  | 'insufficient';

/** Concrete issue types detected during the evidence self-check. */
export type EvidenceIssueType =
  | 'no_primary_legal_basis'
  | 'region_mismatch'
  | 'industry_mismatch'
  | 'only_auxiliary_evidence'
  | 'cross_border_mismatch'
  | 'critical_facts_missing';

/** Retrieval query type generated by the planner. */
export type RetrievalQueryType =
  | 'legal_issue'
  | 'material_fact'
  | 'region_condition'
  | 'industry_condition'
  | 'missing_information';

/** Retriever name used to produce a hit. */
export type RetrieverName =
  | 'keyword'
  | 'vector_mock'
  | 'hybrid'
  | 'elasticsearch'
  | 'pgvector';

/** Evaluation retrieval backend under test. */
export type EvalRetrievalMode = 'service' | 'local';

/** Evaluation review owner under test. */
export type EvalReviewMode = 'llm' | 'local';

/** Optional post-fusion reranker arm for A/B evaluation. */
export type EvalRerankMode = 'off' | 'embedding';

/** Evaluation scenario subset. */
export type EvalSuite = 'quick' | 'base' | 'full';

